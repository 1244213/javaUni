/* Stampare tutti i numeri interi da 10 a -10, in ordine decrescente
– Suggerimento: l’incremento può essere negativo, cioè un decremento */

public class es2 {

    public static void main(String[] args) {

        for (int i = 10; i >= -10; i--)
            System.out.print(i + " ");
    
    System.out.println();
    }
}